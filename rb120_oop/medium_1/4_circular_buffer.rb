class CircularBuffer
  def initialize(size)
    @max_size = size
    @arr = []
  end

  def put(val)
    arr << val
    arr.shift if arr.size > max_size
  end

  def get
    arr.shift
  end

  private
  attr_reader :arr, :max_size
end

buffer = CircularBuffer.new(3)
puts buffer.get == nil

buffer.put(1)
buffer.put(2)
puts buffer.get == 1

buffer.put(3)
buffer.put(4)
puts buffer.get == 2

buffer.put(5)
buffer.put(6)
buffer.put(7)
puts buffer.get == 5
puts buffer.get == 6
puts buffer.get == 7
puts buffer.get == nil

