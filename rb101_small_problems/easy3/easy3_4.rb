# Multiplying Two Numbers

# Input: two numbers
# Output: returned product

# Rules
# - A method with two parameters
# - In the method, multiply them together, and return the result

# Examples/Test Cases
# multiply(5, 3) == 15

# Algorithms
# Define method w/ two numbers `num1` and `num2`
# - multiply and return implicitly
# end method

# Code

def multiply(num1, num2)
  num1 * num2
end

puts (multiply(5, 3) == 15)